                    <p class="question-title">Как проходит исследование? </p>
                    <div class="answer"><p>Ответ: Перед началом обследования у пациента выясняется наличие или отсутствие противопоказаний, при отсутствии противопоказаний пациент допускается к проведению исследования.</p>
                        <p>Внешне МРТ и КТ исследование проходит очень похоже:</p>
                        <p>Пациента укладывают на стол и фиксируют его положение. При необходимости вводят контраст.</p>
                        <p>В случае МРТ, на пациента одеваются специальные пояса или шлемы (радиочастотные катушки) после чего стол задвигается в томограф и начинается сканирование, сопровождаемое достаточно громкими звуками (но переживать не стоит, т.к. пациенту предоставляются наушники или беруши).</p>
                        <p>В случае КТ сканирование пациента происходит в движении в момент прохода интересующей области, через геометрический центр гентри (гентри - основная часть компьютерного томографа). Исследование проходит, без каких либо сильных звуков.</p>
                        <p>В среднем МРТ исследование длится около 30 мин, а КТ около 5 мин. Главная задача пациента не двигаться и выполнять устные инструкции рентген лаборанта или врача. Комната, где расположен аппарат МРТ или КТ всегда оснащена устройством общения с пациентом.</p>
                        <p>Пациенту выдается тревожная кнопка (груша), нажав на которую исследование останавливается.</p>
                        <p>После окончания процедуры лаборант заходит в комнату сканирования, выдвигает стол и помогает пациенту, после чего последнему остается только ждать результаты (чаще всего на пленке и/или на диске).</p>
